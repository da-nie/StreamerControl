//----------------------------------------------------------------------------------------------------
//!задать команду
//----------------------------------------------------------------------------------------------------
void CCommand::Set(const std::string &command,const std::string &device_name)
{
 Command.clear();
 size_t length=command.length();
 Command.reserve(length);
 for(size_t n=0;n<length;n++)
 {
  if (command[n]=='%')
  {
   Command=Command+device_name;
   continue;
  }
  Command=Command+command[n];
 }
}
//----------------------------------------------------------------------------------------------------
//!получить команду
//----------------------------------------------------------------------------------------------------
std::string CCommand::Get(void) const
{
 return(Command);
}
