#pragma pack(1)
class CFile
{
public:
 //-перечисления---------------------------------------------------------------------------------------
 //статус
 enum class STATE
 {
  STATE_EQUIVALENT,///<одинаковый
  STATE_ONLY_IN_HDD,///<есть только на диске
  STATE_ONLY_IN_RECORD,///<есть только в записи
  STATE_LENGTH_ERROR///<разная длина файла
 }; 
 //-структуры------------------------------------------------------------------------------------------
 //-константы------------------------------------------------------------------------------------------
 static const size_t TAR_BLOCK_SIZE=512;///<размер блока TAR
 //-переменные-----------------------------------------------------------------------------------------
 STATE State;///<статус
 std::string FileName;///<имя файла
 std::string FullFileName;///<полное имя файла
 uint64_t Size;///<размер
private:
 //-переменные-----------------------------------------------------------------------------------------
public:
 //-конструктор----------------------------------------------------------------------------------------
 CFile(void);
 //-деструктор-----------------------------------------------------------------------------------------
 ~CFile();
public:
 //-открытые функции-----------------------------------------------------------------------------------
 void Save(FILE *file) const;///<сохранить
 bool Load(FILE *file);///<загрузить
 size_t GetSize(void) const;///<!получить размер
 size_t GetTARSize(void) const;///<получить размер в несжатом TAR-архиве 
private:
 //-закрытые функции-----------------------------------------------------------------------------------
};
