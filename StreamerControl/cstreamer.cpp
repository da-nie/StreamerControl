//----------------------------------------------------------------------------------------------------
//!послать команду стримеру
//----------------------------------------------------------------------------------------------------
bool CStreamer::SendCommand(const std::string &command)
{
 printf("System:%s\r\n",command.c_str());
 if (system(command.c_str())!=0) return(false);
 return(true);
}
